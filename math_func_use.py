import math    # import math library/module
w = -2
x = 3

y = 1
z = -1

print(math.ceil(x))  # round off to nearest integer
print(math.comb(x, y))  # apply combination formula
print(math.copysign(x, z))  # copy sign of 2nd value and apply it to first value
print(math.fabs(z))  # absolute value
print(math.fmod(x, w))  # prefer when we float data


